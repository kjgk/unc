package com.unicorn.core.enumeration;

/**
 * 定义时间单位.
 */
public enum TimeUnit {

    /**
     * 空
     */
    None,

    /**
     * 秒
     */
    Second,

    /**
     * 分钟
     */
    Minute,

    /**
     * 小时
     */
    Hour,

    /**
     * 半小时
     */
    HalfHour,

    /**
     * 天
     */
    Day,

    /**
     * 星期
     */
    Week,

    /**
     * 月
     */
    Month,

    /**
     * 季度
     */
    Quarter,

    /**
     * 年
     */
    Year,
}
